-- admin = auth user whose app_metadata.role is 'admin' (matches isAdminUser in the app)
create or replace function public.is_admin() returns boolean
language sql stable as $$
  select coalesce((auth.jwt() -> 'app_metadata' ->> 'role') = 'admin', false)
$$;

-- locations: anyone reads approved; admins read/write everything
drop policy if exists locations_public_read on public.locations;
create policy locations_public_read on public.locations
  for select using (status = 'approved' or public.is_admin());
drop policy if exists locations_admin_write on public.locations;
create policy locations_admin_write on public.locations
  for all using (public.is_admin()) with check (public.is_admin());

-- pending submissions: anyone can suggest; only admins see/manage the queue
drop policy if exists pending_public_insert on public.pending_submissions;
create policy pending_public_insert on public.pending_submissions
  for insert with check (status = 'pending');
drop policy if exists pending_admin_all on public.pending_submissions;
create policy pending_admin_all on public.pending_submissions
  for all using (public.is_admin()) with check (public.is_admin());

-- saved items: owners only
drop policy if exists saved_plans_owner on public.saved_plans;
create policy saved_plans_owner on public.saved_plans
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);
drop policy if exists saved_places_owner on public.saved_places;
create policy saved_places_owner on public.saved_places
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

-- analytics: insert-only from clients (consent enforced app-side); admins read
drop policy if exists analytics_public_insert on public.analytics_events;
create policy analytics_public_insert on public.analytics_events
  for insert with check (true);
drop policy if exists analytics_admin_read on public.analytics_events;
create policy analytics_admin_read on public.analytics_events
  for select using (public.is_admin());

drop policy if exists impressions_public_insert on public.recommendation_impressions;
create policy impressions_public_insert on public.recommendation_impressions
  for insert with check (true);
drop policy if exists impressions_public_read on public.recommendation_impressions;
create policy impressions_public_read on public.recommendation_impressions
  for select using (true);

drop policy if exists outcomes_public_upsert on public.recommendation_outcomes;
create policy outcomes_public_upsert on public.recommendation_outcomes
  for all using (true) with check (true);

-- user feedback / reviews: owners write their own; reviews are publicly readable
drop policy if exists feedback_owner on public.user_feedback;
create policy feedback_owner on public.user_feedback
  for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

drop policy if exists reviews_public_read on public.location_reviews;
create policy reviews_public_read on public.location_reviews
  for select using (true);
drop policy if exists reviews_owner_write on public.location_reviews;
create policy reviews_owner_write on public.location_reviews
  for insert with check (auth.uid() = user_id);
drop policy if exists reviews_owner_update on public.location_reviews;
create policy reviews_owner_update on public.location_reviews
  for update using (auth.uid() = user_id) with check (auth.uid() = user_id);

-- problem reports & partner inquiries: anyone can submit, only admins read
drop policy if exists reports_public_insert on public.problem_reports;
create policy reports_public_insert on public.problem_reports
  for insert with check (true);
drop policy if exists reports_admin_read on public.problem_reports;
create policy reports_admin_read on public.problem_reports
  for select using (public.is_admin());

drop policy if exists partner_public_insert on public.partner_inquiries;
create policy partner_public_insert on public.partner_inquiries
  for insert with check (status = 'new');
drop policy if exists partner_admin_all on public.partner_inquiries;
create policy partner_admin_all on public.partner_inquiries
  for all using (public.is_admin()) with check (public.is_admin());
