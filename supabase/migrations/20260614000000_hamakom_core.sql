-- ── locations ───────────────────────────────────────────────────────────────
create table if not exists public.locations (
  id             bigint generated by default as identity primary key,
  name           text not null,
  name_he        text,
  city           text not null,
  city_he        text,
  category       text not null,
  occasion       text[] default '{}',
  price          smallint check (price between 1 and 4),
  date_stage     smallint[] default '{1}',
  description    text,
  description_he text,
  maps_query     text,
  kashrus        text,
  featured       boolean default false,
  status         text not null default 'pending' check (status in ('pending','approved','rejected')),
  image_url      text,
  rating         smallint check (rating between 1 and 5),
  hidden_gem     boolean default false,
  notes          text,
  lat            double precision,
  lng            double precision,
  is_partner     boolean default false,
  partner_tier   text,
  created_at     timestamptz default now()
);

-- Columns that may be missing on an older locations table
alter table public.locations add column if not exists rating smallint;
alter table public.locations add column if not exists hidden_gem boolean default false;
alter table public.locations add column if not exists notes text;
alter table public.locations add column if not exists lat double precision;
alter table public.locations add column if not exists lng double precision;
alter table public.locations add column if not exists is_partner boolean default false;
alter table public.locations add column if not exists partner_tier text;
alter table public.locations add column if not exists reservation_url text;
alter table public.locations add column if not exists partner_contact text;

create index if not exists locations_status_idx on public.locations (status);
create index if not exists locations_city_idx on public.locations (city);

-- ── suggest-a-place queue ───────────────────────────────────────────────────
create table if not exists public.pending_submissions (
  id         bigint generated by default as identity primary key,
  name       text not null,
  city       text,
  category   text,
  kashrus    text,
  why        text,
  whatsapp   text,
  date_stage smallint[],
  price      smallint,
  maps_query text,
  occasion   text[],
  status     text not null default 'pending' check (status in ('pending','approved','rejected')),
  created_at timestamptz default now()
