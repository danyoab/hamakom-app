-- ── saved items (signed-in sync) ────────────────────────────────────────────
create table if not exists public.saved_plans (
  user_id    uuid not null references auth.users (id) on delete cascade,
  plan_id    text not null,
  created_at timestamptz default now(),
  primary key (user_id, plan_id)
);

create table if not exists public.saved_places (
  user_id     uuid not null references auth.users (id) on delete cascade,
  location_id bigint not null,
  created_at  timestamptz default now(),
  primary key (user_id, location_id)
);

-- ── analytics ───────────────────────────────────────────────────────────────
create table if not exists public.analytics_events (
  id         bigint generated by default as identity primary key,
  session_id text,
  user_id    uuid,
  event_name text not null,
  item_type  text,
  item_id    text,
  properties jsonb default '{}',
  created_at timestamptz default now()
);

create table if not exists public.recommendation_impressions (
  id                  bigint generated by default as identity primary key,
  session_id          text,
  user_id             uuid,
  quiz_answers        jsonb default '{}',
  primary_plan_id     text,
  backup_location_ids jsonb default '[]',
  created_at          timestamptz default now()
);

create table if not exists public.recommendation_outcomes (
  recommendation_impression_id bigint primary key
    references public.recommendation_impressions (id) on delete cascade,
  saved       boolean,
  shared      boolean,
  maps_opened boolean,
  backups_opened boolean,
  updated_at  timestamptz default now()
);

create table if not exists public.user_feedback (
  user_id       uuid not null,
  item_type     text not null,
  item_id       text not null,
  went          boolean,
  rating        smallint,
  would_do_again boolean,
  notes         text,
  updated_at    timestamptz default now(),
  primary key (user_id, item_type, item_id)
);

-- ── reviews (per-location, per-user) ───────────────────────────────────────
create table if not exists public.location_reviews (
  id          bigint generated by default as identity primary key,
